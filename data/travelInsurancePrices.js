function csvToDict(csvStr) {
  let lines = csvStr.split("\n").map(line => line.split(","));
  const insuredCategory = [
    "Applicant",
    "Insured & Spouse",
    "Insured & Children",
    "Family"
  ];
  const plans = ["Basic", "Enhanced", "Superior", "Premier"];
  prices = lines.map(line => {
    let period = line[0];
    if (!isNaN(period)) parseInt(period, 10);
    let dict = { period };
    let index = 1;
    insuredCategory.forEach(cat => {
      dict[insuredCategory] = {};
      plans.forEach(plan => {
        const price = line[index];
        dict[insuredCategory][plan] = parseInt(price, 10);
        index++;
      });
    });
    return dict;
  });
  return prices;
}

